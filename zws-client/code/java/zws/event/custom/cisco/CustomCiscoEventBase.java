import zws.util.RoutedEventBase;

public class CustomCiscoEventBase extends RoutedEventBase {
	public String getAction() { return getString(ACTION); }
	public void setAction(String s) { set(ACTION, s); }

  public String getAgileDomainName() { return (String)get(AGILE_DOMAIN_NAME); }
  public void setAgileDomainName(String s) { set(AGILE_DOMAIN_NAME, s); }
  public String getAgileServerName() { return (String)get(AGILE_SERVER_NODE); }
  public void setAgileServerName(String s) { set(AGILE_SERVER_NODE, s); }
	public void setAgileDatasourceName(String s) { set(AGILE_DATASOURCE_NAME, s); }
  public String getAgileDatasourceName() { return (String)get(AGILE_DATASOURCE_NAME); }
	
  private static String ACTION="action";
  public static String AGILE_DOMAIN_NAME="agile-domain";
  public static String AGILE_SERVER_NODE="agile-server";
  public static String AGILE_DATASOURCE_TYPE="agile-datasource";
  public static String AGILE_DATASOURCE_NAME="agile-datasource-name";
  
}
