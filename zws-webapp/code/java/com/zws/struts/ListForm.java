package com.zws.struts;

import java.util.Collection;
import java.util.Vector;

import org.apache.struts.action.ActionForm;

public class ListForm extends ActionForm {
  private String action = null;
  private String[] selectedItems = null;
  private Collection items = new Vector();

  public ListForm(){}
  public Collection getItems() { return items; }
  public void setItems(Collection c) { items = c; }

  public String[] getSelectedItems() { return selectedItems; }
  public void setSelectedItems(String[] x) { selectedItems = x; }

  public String getAction() { return action; }
  public void setAction(String x) { action = x; }
}
