package com.zws.hi;
import org.apache.struts.action.ActionError;

class Log extends ActionError {
  public Log(String key) { super(key); }
  public Log(String key, Object arg1) { super(key, arg1); }
  public Log(String key, Object arg1, Object arg2) { super(key, arg1, arg2); }
  public Log(String key, Object arg1, Object arg2, Object arg3) { super(key, arg1, arg2, arg3); }
}
